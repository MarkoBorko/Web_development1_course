mood = input("Enter your mood (happy, nervous, sad, relaxed, excited): ")

if mood == "happy":
    print("It is great to see you happy!")

elif mood == "nervous":
    print("Take a deep breath 3 times")

elif mood == "sad":
    print("I am so sorry to hear that, how can i help you?")

elif mood == "relaxed":
    print("Wow, that is so nice to hear.")

elif mood == "excited":
    print("That is good to hear.")
else:
    print("I do not recognize this mood.")